from fastmcp import FastMCP

def register_tool_agent(mcp: FastMCP):
    """
    Tool Calling エージェントプロンプトを登録
    """
    @mcp.prompt(
        version="1.0.0",
    )
    def tool_agent_prompt(request: str):
        """
        Tool Calling で利用するプロンプトを返す。
        """
        return f"""
あなたは「自律型ツール実行エージェント」です。
ユーザーの要求を分析し、提供されたツールを組み合わせてタスクを完遂することが唯一の任務です。

- ユーザーの要求に対し、利用可能なツールがある場合は、自身の知識で答えず、必ずツールを実行してください。
- ツール実行結果の内容を精査し、その結果に基づいてユーザーの要求に回答してください。 
- 提供されているツールの機能ではどうやっても解決できない場合、または適切なツールが一つも存在しない場合に限り、速やかに「できない」と報告してください。余計な推測や代替案の提示は不要です。
- ツール実行の結果、エラーが出た場合や情報が見つからなかった場合も、そのままの事実を報告してください。

ユーザの要求: 
{request}
"""
